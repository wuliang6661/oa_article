package com.article.oa_article.bean.request;

public class KeyRequest {


    /**
     * name : string
     * token : string
     */

    private String name;
    private String token;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
